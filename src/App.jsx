  // ==========================================
  // ‚öîÔ∏è 3. COMBAT SYSTEM 
  // ==========================================
  const combat = useCombat(
    player, 
    setPlayer, 
    setLogs, 
    advanceDungeon, 
    exitDungeon, 
    inDungeon
  ); 
  
  const { isCombat, gameState, currentMap, handleSelectMap } = combat;

  // ‚úÖ ‡πÄ‡∏ä‡∏∑‡πà‡∏≠‡∏° Map ‡∏õ‡∏±‡∏à‡∏à‡∏∏‡∏ö‡∏±‡∏ô‡∏Å‡∏•‡∏±‡∏ö‡πÑ‡∏õ‡πÉ‡∏´‡πâ Travel
  travel.currentMap = currentMap;

  // ‚úÖ [‡∏™‡∏≥‡∏Ñ‡∏±‡∏ç‡∏°‡∏≤‡∏Å] ‡∏Å‡∏≤‡∏£ "‡πÄ‡∏™‡∏µ‡∏¢‡∏ö‡∏õ‡∏•‡∏±‡πä‡∏Å" ‡∏ã‡πâ‡∏≥‡∏≠‡∏µ‡∏Å‡∏£‡∏≠‡∏ö‡πÄ‡∏û‡∏∑‡πà‡∏≠‡∏Ñ‡∏ß‡∏≤‡∏°‡∏ä‡∏±‡∏ß‡∏£‡πå
  combat.advanceDungeon = advanceDungeon;
