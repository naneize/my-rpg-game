  const collectionBonuses = calculateCollectionBonuses(player.inventory, monsters);

  // ==========================================
  // üé≠ 5. VIEW RENDERER (‡∏à‡∏±‡∏î‡∏Å‡∏≤‡∏£‡∏Å‡∏≤‡∏£‡πÅ‡∏™‡∏î‡∏á‡∏ú‡∏•‡∏´‡∏ô‡πâ‡∏≤‡∏à‡∏≠)
