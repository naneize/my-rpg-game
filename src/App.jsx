    setPlayer,
    setLogs,
    collScore,
    passiveBonuses,
    collectionBonuses, // ‚úÖ ‡∏™‡πà‡∏á‡∏Å‡πâ‡∏≠‡∏ô‡πÇ‡∏ö‡∏ô‡∏±‡∏™‡∏™‡∏∞‡∏™‡∏°‡∏Ñ‡∏£‡∏ö‡πÄ‡∏ã‡∏ï‡πÄ‡∏Ç‡πâ‡∏≤‡πÑ‡∏õ‡πÉ‡∏ô‡∏£‡∏∞‡∏ö‡∏ö‡πÅ‡∏™‡∏î‡∏á‡∏ú‡∏•
